function displayBrief() {
    var briefElement = document.getElementById('homeSelection');
    
    // Check if the brief is already visible
    if (briefElement.style.display === 'block') {
      briefElement.style.display = 'none'; // Hide the brief
    } else {
      briefElement.style.display = 'block'; // Show the brief
    }
  }

